# Functions - collection of reusable code blocks
# starts with 'def' keyword
# then has the name of the function
# list of parameters/arguments
# the lines of code

# Parameters - variables defined inside the function
# Arguments - values passed to the function

def greet(fname, lname):
    print(f"Hello {fname} {lname}!")

# execute/call the function
greet("Matt", "Adams")

greet("First", "Last")